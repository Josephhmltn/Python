name = input("Enter file:")
if len(name) < 1 :
    name = "mbox-short.txt"
handle = open(name)
counts = dict()
for line in handle :
    if line.startswith('From ') :
        words = line.split()
        #print(words)
        for word in words :
            if word.__contains__('@') :
                counts[word] = counts.get(word,0) + 1
    continue            
#print(counts)            
maxcount = None
maxword = None
for word,count in counts.items() :
    if maxcount is None or count > maxcount:
        maxword = word
        maxcount = count
print(maxword, maxcount)